package com.javaConditionalLoops;

public class Loop_while
{

	public static void main(String[] args) 
	{	
		/**
		 * 
		 * initialization;
		 * 
		 * while(condition
		 * {
		 * 		//print statement
		 * 		//increment/decrement statement
		 * }
		 * 
		 */
		
	//while loop
			//initialization;
			int a = 5;
			
			//condition
			while(a <= 10)
			{
				System.out.println(a);
				a++;
			}
	}

}
