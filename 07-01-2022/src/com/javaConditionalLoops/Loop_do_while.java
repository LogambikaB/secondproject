package com.javaConditionalLoops;   

public class Loop_do_while 
{

	public static void main(String[] args) 
	{
			/**
			 * 
			 * initialization;
			 * 
			 * do
			 * {
			 * 		//print statement
			 * 		//increment/decrement statement
			 * }
			 * while(condition);
			 * 
			 */
	//do-while loop
			//initialization;
			int a = 1;

			//condition
			do
			{
				System.out.println(a);
				a++;
			}
			while(a <= -5);
			
		}

	}



