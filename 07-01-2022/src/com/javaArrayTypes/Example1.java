package com.javaArrayTypes;

/**
 *   Single Dimensional Array: 
 *
 */

public class Example1 
{
	//single dimensional array in "one line"
		public static void main(String[] args)
		{

	//declaration, instantiation and initialization of an array in "one line"
			int customerIds[] = {2000, 2005, 2010, 2015, 2020};

			for(int i = 0; i < customerIds.length; i++)
			{
				System.out.println(customerIds[i]);
			}


		}

	
}
