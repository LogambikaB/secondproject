package com.javaArrays;

public class Example2 
{
	/**
	 * WAP to print customer id's using java array.
	 */
	
		public static void main(String[] args) 
		{
			int customerIds[] = {2000, 2005, 2010, 2015, 2020};
		
		for(int i = 0; i < customerIds.length; i++) // customer id's length = 5
			{
				System.out.println(customerIds[i]);
			
			}
		
		}

}
