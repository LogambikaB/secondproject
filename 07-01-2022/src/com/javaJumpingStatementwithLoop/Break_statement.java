package com.javaJumpingStatementwithLoop;

public class Break_statement 
{
	//break statement
		public static void main(String[] args) 
		{
			
			for(int i = 1; i <= 10; i++)
			{
				
				if(i == 6)
				{
					break;				// break statement [ will "stop execution" from that particular part ]
				}
				
				System.out.println(i);
			}
			
			System.out.println("#########################");
			
	//break statement
			for(int i = 1; i <= 10; i++)
			{
				
				if(i == 8)
				{
					break;				// break statement [ will "stop execution" from that particular part ]
				}
				
				System.out.println(i);
			}
			
			
		}


}


