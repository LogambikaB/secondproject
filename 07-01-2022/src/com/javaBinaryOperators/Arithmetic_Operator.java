package com.javaBinaryOperators;

public class Arithmetic_Operator 
{

	public static void main(String[] args)
	{
		//Arithmetic Operators
		int a = 100;
		int b = 10;

		System.out.println("+: "+(a + b));
		System.out.println("-: "+(a - b));	
		System.out.println("*: "+(a * b));
		System.out.println("/: "+(a / b));
		System.out.println("%: "+(a % b));
		
	}

	

}
